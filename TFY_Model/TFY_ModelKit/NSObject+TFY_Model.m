    NSDictionary *dic = [self _tfy_dictionaryWithJSON:json];
    return [self tfy_modelWithDictionary:dic];
}

+ (instancetype)tfy_modelWithDictionary:(NSDictionary *)dictionary {
    if (!dictionary || dictionary == (id)kCFNull) return nil;
    if (![dictionary isKindOfClass:[NSDictionary class]]) return nil;
    
    Class cls = [self class];
    TFY_ModelMeta *modelMeta = [TFY_ModelMeta metaWithClass:cls];
    if (modelMeta->_hasCustomClassFromDictionary) {
        cls = [cls tfy_modelCustomClassForDictionary:dictionary] ?: cls;
    }
    
    NSObject *one = [cls new];
    if ([one tfy_modelSetWithDictionary:dictionary]) return one;
    return nil;
}

